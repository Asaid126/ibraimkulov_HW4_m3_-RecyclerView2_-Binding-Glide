package com.example.ibraimkulov_hw4_m3_aaa.continent;

public class ContinentModel {
    private String img;
    private String name;

    public ContinentModel(String img, String name) {
        this.img = img;
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}